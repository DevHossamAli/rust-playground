/////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Simple Match Example..
/////////////////////////////////////////////////////////////////////////////////////////////////////////////

fn main() {
    let num = 6;

    match num {
        1 => println!("Number 2"),
        2 => println!("Number 3"),
        3 | 4 => println!("Number is either 3 or 4"),
        5...10 => println!("Number is between 5 and 10"),
        _ => println!("No Match Found!")
    }
    
}